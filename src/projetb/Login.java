    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:

        String email = champEmail.getText();
        String mdp = champMdp.getText();
        Utilisateur utilisateur = UtilisateurData.login(email, mdp);
        if (utilisateur == null) {
            JOptionPane.showMessageDialog(null, "Email out mot de pass est incorrect", "Message", JOptionPane.ERROR_MESSAGE);
        } else {
            if (utilisateur.getStatut().equals("")) {
                JOptionPane.showMessageDialog(null, "Utilisateur n'est pas encore activé par l'admin", "Message", JOptionPane.INFORMATION_MESSAGE);

            } else {
                this.dispose();
                if (utilisateur.getEmail().equals("admin@gmail.com")) {
                    new HomeAdmin(utilisateur).setVisible(true);
                }
                if (utilisateur.getRôle().equals("Responsable  restaurant")) {
                    new HomeRestau(utilisateur).setVisible(true);
                }
                if (utilisateur.getRôle().equals("Responsable point de vente")) {
                    new HomePointVente(utilisateur).setVisible(true);
                }
            }
        }

    }//GEN-LAST:event_btnLoginActionPerformed

