    public void validerChamps() {
        String email = champEmail.getText();
        String mdp = champMdp.getText();
        if (email.matches(emailPattern) && !mdp.equals("")) {
            btnLogin.setEnabled(true);
        } else {
            btnLogin.setEnabled(false);
        }
    }

