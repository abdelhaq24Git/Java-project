    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int i=jTable1.getSelectedRow();
        TableModel model=jTable1.getModel();
        int id=Integer.parseInt(model.getValueAt(i, 0).toString());
        String produit=model.getValueAt(i, 1).toString();
        String date=model.getValueAt(i, 2).toString();
        int a=JOptionPane.showConfirmDialog(null, "Voulez vous vraiment marquer le produit comme acquis ?","Selectionner",JOptionPane.YES_NO_OPTION);
        if(a==0){
           ProduitDemandéData.supprimer(id);
           this.dispose();
           new CréationBesoin(Actuel).setVisible(true);
        }
        
        
    }//GEN-LAST:event_jTable1MouseClicked

