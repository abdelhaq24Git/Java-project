    public CréationBesoin(Utilisateur utilisateur) {
        Actuel=utilisateur;
        initComponents();
        btnAjouter.setEnabled(false);
        labelID.setText(Actuel.getId()+"");
        DefaultTableModel defaultModel = (DefaultTableModel)jTable1.getModel();
        ArrayList<ProduitDemandé> list=BesoinData.getAll(Actuel);
        Iterator<ProduitDemandé> itr=list.iterator();
        while(itr.hasNext()){
            ProduitDemandé p=itr.next();
            defaultModel.addRow(new Object[]{p.getId(),p.getProduit(),p.getQuantité(),p.getDate()});
        }
    }
